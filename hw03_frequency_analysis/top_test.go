func TestTop10_Additional(t *testing.T) {
	tests := []struct {
		name     string
		text     string
		expected []string
	}{
		{
			name: "multiple chars",
			text: "a b a b a c",
			expected: []string{
				"a", // 3
				"b", // 2
				"c", // 1
			},
		},
		{
			name: "multiple words",
			text: "hello world hello world twice",
			expected: []string{
				"hello", // 2
				"world", // 2
				"twice", // 1
			},
		},
		{
			name: "multiple words with punctuation",
			text: `test completed test! completed test, completed test? completed test. completed test^ completed 
						test* completed test@ completed test+ completed test" completed a a a a a a a a`,
			expected: []string{
				"completed", // 10
				"test",      // 10
				"a",         // 9
			},
		},
		{
			name: "large fragment of text",
			text: bigText,
			expected: []string{
				"—",       // 51
				"в",       // 32
				"и",       // 21
				"не",      // 15
				"человек", // 14
				"черном",  // 14
				"стрелок", // 12
				"это",     // 10
				"как",     // 9
				"что",     // 9
			},
		},
		{
			name:     "empty text",
			text:     "",
			expected: []string{},
		},
	}

	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			got := Top10(tt.text)
			require.Equal(t, tt.expected, got, "got %v, want %v", got, tt.expected)
		})
	}
}
